package no.jchief.webapp.imperialassault.model;

public class Attack {
	public int numRedDices;
	public int numYellowDices;
	public int numBlueDices;
	public int numGreenDices;
	
	public Attack() {
		super();
	}

	public int getNumRedDices() {
		return numRedDices;
	}

	public void setNumRedDices(int numRedDices) {
		this.numRedDices = numRedDices;
	}

	public int getNumYellowDices() {
		return numYellowDices;
	}

	public void setNumYellowDices(int numYellowDices) {
		this.numYellowDices = numYellowDices;
	}

	public int getNumBlueDices() {
		return numBlueDices;
	}

	public void setNumBlueDices(int numBlueDices) {
		this.numBlueDices = numBlueDices;
	}

	public int getNumGreenDices() {
		return numGreenDices;
	}

	public void setNumGreenDices(int numGreenDices) {
		this.numGreenDices = numGreenDices;
	}
	
	
	
	

}
