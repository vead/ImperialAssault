package no.jchief.webapp.imperialassault.model;

public class Defence {
	public int numBlackDices;
	public int numWhiteDices;
	
	public Defence() {
		super();
		
	}
	
	public int getNumBlackDices() {
		return numBlackDices;
	}
	public void setNumBlackDices(int numBlackDices) {
		this.numBlackDices = numBlackDices;
	}
	public int getNumWhiteDices() {
		return numWhiteDices;
	}
	public void setNumWhiteDices(int numWhiteDices) {
		this.numWhiteDices = numWhiteDices;
	}

}
